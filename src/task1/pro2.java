package task1;

public class pro2 {
	public static void main(String[] args) {
        // Declare and initialize the integer variable
        int number = 42; // Example value, you can change this to test with other numbers

        // Check if the number is even
        if (number % 2 == 0) {
            // Print message if the number is even
            System.out.println(number + " is an even number.");
        } else {
            // Print message if the number is not even (i.e., odd)
            System.out.println(number + " is an odd number.");
        }
    }
}
